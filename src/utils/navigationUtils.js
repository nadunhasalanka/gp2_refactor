import React from 'react';

// Helper function to get proper home route based on user role
export const getHomeRouteByRole = (role) => {
    switch(role) {
        case 'client':
            return '/client/dashboard';
        case 'junior_lawyer':
            return '/junior/dashboard';
        case 'admin':
            return '/admin/dashboard';
        case 'lawyer':
        default:
            return '/lawyer/dashboard';
    }
};

// Helper function to get display-friendly role name
export const getRoleDisplayName = (role) => {
    switch(role) {
        case 'client':
            return 'Client';
        case 'junior_lawyer':
            return 'Junior Lawyer';
        case 'lawyer':
            return 'Senior Lawyer';
        case 'admin':
            return 'Administrator';
        default:
            return 'User';
    }
};

